# Функция для последовательного (линейного) поиска элемента в списке
def linear_search(arr, target):
    """
    Алгоритм линейного поиска.
    Последовательно проходит по всем элементам списка 'arr'
    и сравнивает каждый элемент с искомым 'target'.
    Если элемент найден — возвращает его индекс.
    Если элемент отсутствует — возвращает -1.
    """
    
    # Проходим по всем элементам списка с помощью индекса
    for i in range(len(arr)):
        # Сравниваем текущий элемент с искомым
        if arr[i] == target:
            # Если нашли совпадение — возвращаем индекс
            return i
    
    # Если элемент не найден — возвращаем -1
    return -1


# Основная часть программы
if __name__ == "__main__":
    # Пользователь вводит количество элементов списка
    n = int(input("Введите количество элементов списка: "))

    # Создаём пустой список
    arr = []

    # Вводим элементы списка
    print(f"Введите {n} элементов:")
    for i in range(n):
        value = input(f"Элемент {i + 1}: ")
        arr.append(value)

    # Пользователь вводит элемент, который нужно найти
    target = input("\nВведите элемент для поиска: ")

    # Вызываем функцию линейного поиска
    result = linear_search(arr, target)

    # Проверяем результат
    if result != -1:
        print(f"Элемент '{target}' найден в списке на позиции {result} (индекс {result}).")
    else:
        print(f"Элемент '{target}' не найден в списке.")
