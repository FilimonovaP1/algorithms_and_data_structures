#include <iostream>  // подключаем стандартную библиотеку для ввода/вывода
using namespace std;

// Функция для обмена значений двух переменных
void swap(int &a, int &b) {
    int temp = a;
    a = b;
    b = temp;
}

// Функция "разделения" массива (partition)
// Она выбирает "опорный" элемент (pivot) и переставляет элементы так,
// что все меньшие pivot оказываются слева, а большие — справа.
int partition(int arr[], int low, int high) {
    int pivot = arr[high]; // выбираем последний элемент как опорный
    int i = low - 1;       // индекс меньшего элемента

    // Проходим по массиву и размещаем элементы по обе стороны от pivot
    for (int j = low; j < high; j++) {
        if (arr[j] < pivot) {  // если текущий элемент меньше опорного
            i++;               // увеличиваем индекс меньших элементов
            swap(arr[i], arr[j]); // меняем местами arr[i] и arr[j]
        }
    }

    // После цикла ставим опорный элемент на правильное место
    swap(arr[i + 1], arr[high]);
    return i + 1; // возвращаем индекс опорного элемента
}

// Рекурсивная функция быстрой сортировки
void quickSort(int arr[], int low, int high) {
    if (low < high) {
        // Находим индекс опорного элемента после разделения
        int pi = partition(arr, low, high);

        // Рекурсивно сортируем левую и правую части относительно pivot
        quickSort(arr, low, pi - 1);  // левая часть
        quickSort(arr, pi + 1, high); // правая часть
    }
}

// Вспомогательная функция для вывода массива
void printArray(int arr[], int size) {
    for (int i = 0; i < size; i++)
        cout << arr[i] << " ";
    cout << endl;
}

// Главная функция программы
int main() {
    int n;

    cout << "Введите количество элементов массива: ";
    cin >> n; // считываем размер массива

    int arr[n]; // создаём массив заданного размера

    cout << "Введите " << n << " элементов массива:\n";
    for (int i = 0; i < n; i++) {
        cin >> arr[i]; // вводим элементы
    }

    cout << "\nМассив до сортировки: ";
    printArray(arr, n);

    // Вызов быстрой сортировки
    quickSort(arr, 0, n - 1);

    cout << "Массив после сортировки: ";
    printArray(arr, n);

    return 0; // успешное завершение программы
}
